const models = require("../models");

const browse = (req, res) => {
  const status = req.query.status ? req.query.status : "0";
  const duree = req.query.duree ? req.query.duree : "0";
  models.decisions
    .readfilter(status, duree)
    .then(([rows]) => {
      res.send(rows);
    })
    .catch((err) => {
      console.error(err);
      res.sendStatus(500);
    });
};

const browseWithUserId = (req, res) => {
  models.decisions
    .findWithUserId(req.params.id)
    .then(([rows]) => {
      res.send(rows);
    })
    .catch((err) => {
      console.error(err);
      res.sendStatus(500);
    });
};

const read = (req, res) => {
  models.decisions
    .find(req.params.id)
    .then(([rows]) => {
      if (rows[0] == null) {
        res.sendStatus(404);
      } else {
        res.send(rows[0]);
      }
    })
    .catch((err) => {
      console.error(err);
      res.sendStatus(500);
    });
};

const edit = (req, res) => {
  const decisions = req.body;
  // TODO validations (length, format...)
  decisions.id = parseInt(req.params.id, 10);
  models.decisions
    .update(decisions)
    .then(([result]) => {
      if (result.affectedRows === 0) {
        res.sendStatus(404);
      } else {
        res.sendStatus(204);
      }
    })
    .catch((err) => {
      console.error(err);
      res.sendStatus(500);
    });
};

const statusedit = (req, res) => {
  const { id } = req.params;
  const { status } = req.params;
  models.decisions
    .updatestatus(id, status)
    .then(([result]) => {
      if (result.affectedRows === 0) {
        res.sendStatus(404);
      } else {
        res.sendStatus(204);
      }
    })
    .catch((err) => {
      console.error(err);
      res.sendStatus(500);
    });
};

const add = (req, res) => {
  const decisions = req.body;

  models.decisions
    .insert(decisions)
    .then(([result]) => {
      res.location(`/decisions/${result.insertId}`).sendStatus(201);
    })
    .catch((err) => {
      console.error(err);
      res.sendStatus(500);
    });
};

const destroy = (req, res) => {
  models.decisions
    .delete(req.params.id)
    .then(([result]) => {
      if (result.affectedRows === 0) {
        res.sendStatus(404);
      } else {
        res.sendStatus(204);
      }
    })
    .catch((err) => {
      console.error(err);
      res.sendStatus(500);
    });
};

module.exports = {
  browse,
  read,
  edit,
  statusedit,
  add,
  destroy,
  browseWithUserId,
};
